export function getRelatedSocialPosts(slug, n = 4) {
  const idx = SOCIAL_POSTS.findIndex((p) => p.slug === slug);
  if (idx === -1) return [];
  const out = [];
  for (let i = 1; i <= SOCIAL_POSTS.length && out.length < n; i++) {
    out.push(SOCIAL_POSTS[(idx + i) % SOCIAL_POSTS.length]);
  }
  return out;
}
